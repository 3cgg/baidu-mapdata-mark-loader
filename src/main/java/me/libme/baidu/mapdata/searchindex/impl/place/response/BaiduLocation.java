package me.libme.baidu.mapdata.searchindex.impl.place.response;

public class BaiduLocation {
	private float lat;//纬度
	private float lng;//经度
	public float getLat() {
		return lat;
	}
	public void setLat(float lat) {
		this.lat = lat;
	}
	public float getLng() {
		return lng;
	}
	public void setLng(float lng) {
		this.lng = lng;
	}
	
	
}
